function setLanguage(lang) {
  localStorage.setItem("lang", lang);
  location.reload();
}

function applyLanguage() {
  const lang = localStorage.getItem("lang") || "tr";
  const page = window.location.pathname;
  

  if (page.includes("index.html") || page === "/") {
    document.title = translations[lang].index.title;
    document.querySelector(".intro").textContent = translations[lang].index.intro;
    document.querySelector(".btn.primary").textContent = translations[lang].index.hakkimda;
    document.querySelector(".btn.secondary").textContent = translations[lang].index.cvindir;

  } else if (page.includes("about.html")) {
    document.title = translations[lang].about.title;
    document.querySelector("h2").textContent = translations[lang].about.baslik;
    document.querySelector(".section p").textContent = translations[lang].about.paragraf;

    const headers = document.querySelectorAll(".section h2");
    headers[1].textContent = translations[lang].about.egitim;
    headers[2].textContent = translations[lang].about.is;
    headers[3].textContent = translations[lang].about.teknik;
    headers[4].textContent = translations[lang].about.sertifika;
    headers[5].textContent = translations[lang].about.ilgi;

    const uls = document.querySelectorAll(".section ul");
    uls[0].innerHTML = `<li>${translations[lang].about.egitim_icerik}</li>`;
    uls[1].innerHTML = translations[lang].about.is_icerik.map(i => `<li>${i}</li>`).join('');
    uls[2].innerHTML = translations[lang].about.teknik_icerik.map(i => `<li>${i}</li>`).join('');
    uls[3].innerHTML = translations[lang].about.sertifika_icerik.map(i => `<li>${i}</li>`).join('');
    uls[4].innerHTML = translations[lang].about.ilgi_icerik.map(i => `<li>${i}</li>`).join('');

  } else if (page.includes("projects.html")) {
  document.title = translations[lang].projects.title;
  document.querySelector("h2").textContent = translations[lang].projects.baslik;

  const cards = document.querySelectorAll(".project-card");
  const data = translations[lang].projects.list;
  cards.forEach((card, i) => {
    card.querySelector("h3").textContent = data[i].title;
    card.querySelector("p").textContent = data[i].desc;
  });

  // Web Sitesi butonlarını güncelle
  const buttons = document.querySelectorAll(".project-card .btn.secondary");
  buttons.forEach(btn => {
    btn.textContent = translations[lang].projects.button;
  });
} else if (page.includes("contact.html")) {
    document.title = translations[lang].contact.title;
    document.querySelector("h2").textContent = translations[lang].contact.contact;
    const info = document.querySelectorAll(".contact-info p");
    info[0].innerHTML = `<strong>${translations[lang].contact.location}</strong> Gaziantep, Turkey`;
    info[1].innerHTML = `<strong>${translations[lang].contact.phone}</strong> +90 533 919 09 07`;
    info[2].innerHTML = `<strong>${translations[lang].contact.email}</strong> <span class="email">eyypozgur@gmail.com</span>`;
    document.querySelector(".btn.primary").textContent = translations[lang].contact.send;
  }
}

document.addEventListener("DOMContentLoaded", applyLanguage);

function toggleMenu() {
  const menu = document.querySelector(".nav-links");
  menu.classList.toggle("active");
}